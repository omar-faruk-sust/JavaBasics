public class PrimitiveDataTypes {

    public static void main(String[] args) {
        char myInitial = 'O';
        System.out.println("My initial is: " + myInitial);

        String welcomeSentence = "Hello, Welcome to java world.";
        System.out.println(welcomeSentence);

        int speed = 90;
        System.out.println(speed);    // print int

        short temperature = -200;
        System.out.println(temperature);  // prints -200

        long longRange = 42332200000L;
        System.out.println(longRange);    // prints -42332200000

        float amount = 6.99f;
        System.out.println(amount);    // prints float

        double weight = 70.6458763d;
        System.out.println(weight);    // prints double

        boolean flag = true;
        System.out.println(flag);    // prints true
    }
}
